package Practice;

public class ThirdSwapNumber {

	public static void main(String[] args) {
		int n3;
		int n1=2;
		int n2=5;
		
		System.out.println("Before Swapping Number");
		
		System.out.println("Print Value of n1="+n1);
		System.out.println("Print value of n2="+n2);
		
		n3=n1; //n3=2
	
		n1=n2; //n1=5
		n2=n3; //n2=2
		
		System.out.println("After Swapping Number");
		System.out.println("Print value of n1="+n1);
		System.out.println("Print value of n2="+n2);
	}

}
